package Portfolio.CalculusCalc;
import java.util.*;

public class Term
{
    public String TopOfFunct;
    public String BotOfFunct;

    public Term()
    {
        TopOfFunct = "";
        BotOfFunct = "";
    }
    public Term(String T, String B)
    {
        TopOfFunct = T;
        BotOfFunct = B;
    }
   
}
